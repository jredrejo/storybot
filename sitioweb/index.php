  <section id="fases">
    <div class="wrap">
      <div class="section-head reveal">
        <span class="eyebrow">Proceso</span>
        <h2>Fases de desarrollo del proyecto</h2>
        <p>Del diagnóstico inicial a la validación técnica de siete robots plenamente operativos.</p>
      </div>
      <div class="timeline">
        <div class="phase reveal"><div class="num">1</div><div class="phase-card">
          <span class="when">Enero</span>
          <h3>Diagnóstico y diseño del proyecto</h3>
          <p>Constitución del comité intercentros, revisión normativa y reuniones con las escuelas infantiles para identificar necesidades pedagógicas y tecnológicas.</p>
        </div></div>
        <div class="phase reveal"><div class="num">2</div><div class="phase-card">
          <span class="when">Febrero</span>
          <h3>Programa Muévete y diseño técnico-pedagógico</h3>
          <p>Presentación del proyecto y definición de la arquitectura electrónica, el sistema de audio, la iluminación LED emocional, los botones y el lector de pictogramas.</p>
        </div></div>
        <div class="phase reveal"><div class="num">3</div><div class="phase-card">
          <span class="when">Formación</span>
          <h3>Formación especializada del alumnado</h3>
          <p>Dos jornadas intensivas de narración oral y creatividad: técnicas de cuentacuentos, dramatización, modulación de la voz, expresión corporal y diseño narrativo.</p>
        </div></div>
        <div class="phase reveal"><div class="num">4</div><div class="phase-card">
          <span class="when">Desarrollo</span>
          <h3>Desarrollo tecnológico del sistema</h3>
          <p>El alumnado del IES Santiago Apóstol diseñó, programó y construyó los robots. El objetivo de un prototipo se amplió a <b>siete unidades completamente operativas</b>.</p>
        </div></div>
        <div class="phase reveal"><div class="num">5</div><div class="phase-card">
          <span class="when">Materiales</span>
          <h3>Elaboración de materiales pedagógicos</h3>
          <p>Cuentos originales e interactivos con finales alternativos, grabaciones de voz, guías metodológicas, materiales para alumnado con NEAE y una biblioteca de más de diez cuentos.</p>
        </div></div>
        <div class="phase reveal"><div class="num">6</div><div class="phase-card">
          <span class="when">Intercentros</span>
          <h3>Visita técnica al IES Santiago Apóstol</h3>
          <p>El alumnado de Educación Infantil conoció las instalaciones, observó la fabricación de los robots y participó en demostraciones y propuestas de mejora.</p>
        </div></div>
        <div class="phase reveal"><div class="num">7</div><div class="phase-card">
          <span class="when">Mayo – Junio</span>
          <h3>Validación técnica y preparación del pilotaje</h3>
          <p>Pruebas de audio, botones, LEDs, estabilidad, usabilidad y seguridad. El pilotaje en las escuelas infantiles se realizará en <b>septiembre de 2026</b>.</p>
        </div></div>
      </div>
