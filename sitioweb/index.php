            <li><b>Pantalla táctil</b> de 7" HDMI</li>
            <li><b>Lector NFC</b> ACR122U + tarjetas NTAG213</li>
          </ul>
        </div>
        <div class="tech-card reveal">
          <h3><span class="ico">💡</span> Interacción física</h3>
