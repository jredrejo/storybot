  <section id="introduccion">
    <div class="wrap">
      <div class="section-head reveal">
        <span class="eyebrow">Introducción</span>
        <h2>Tecnología con propósito para las aulas de 0 a 6 años</h2>
        <p>StoryBot nace de la convergencia entre dos necesidades reales del territorio extremeño.</p>
      </div>
      <div class="intro-cols">
        <div class="reveal">
          <p class="lead-first">Las escuelas infantiles demandan cada vez más recursos innovadores que incorporen la tecnología desde edades tempranas de forma segura, significativa e inclusiva. Aunque la robótica educativa está implantada en etapas superiores, apenas existen herramientas adaptadas a niños y niñas menores de seis años, especialmente en contextos rurales.</p>
          <p>Por otro lado, la Formación Profesional necesita experiencias de aprendizaje auténticas, conectadas con problemas reales del entorno. StoryBot une ambas necesidades: un <b>robot cuentacuentos</b> que integra narrativa, robótica, accesibilidad y educación emocional en un único recurso.</p>
          <p>El proyecto se fundamenta en el <b>Aprendizaje Basado en Proyectos</b>, el <b>Aprendizaje Servicio</b> y la colaboración interdisciplinar, situando al alumnado como protagonista del diseño, construcción y validación del producto final.</p>
        </div>
        <div class="intro-card reveal">
          <h3>¿Qué hace StoryBot?</h3>
          <ul>
            <li>Narra cuentos en audio al acercar una tarjeta NFC</li>
            <li>Cuentos interactivos donde los niños eligen el camino de la historia</li>
            <li>Genera cuentos con inteligencia artificial ejecutada localmente</li>
            <li>Iluminación LED que acompaña las emociones del relato</li>
            <li>Pulsadores físicos y pictogramas para un uso inclusivo</li>
            <li>Funciona 100% sin conexión a internet, de forma segura</li>
          </ul>
        </div>
      </div>
    </div>
